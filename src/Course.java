public class Course {
	
	int courseID;
	String courseName;
	
	public int getCourseID() {
		return this.courseID;
	}
	
	public void setCourseID(int newID) {
		this.courseID = newID;
	}
	
	public String getCourseName() {
		return this.courseName;
	}
	
	public void setCourseName(String newName) {
		this.courseName = newName;
	}
	
}
